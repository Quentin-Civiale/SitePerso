    <section>
        <article>
            <h3>Scénario</h3>
            <hr>
            <p>Vous venez d'être embauché comme chef de projet dans l'agence web "Digitalizer".<br/> Pour votre première mission, votre directeur vous a chargé de reprendre le dossier d'un de vos collègues. Pour la passation de dossier, ce collègue vous a redigé "un brief".</p>
            <br/>
        </article>

        <article>
            <h3>Brief client</h3>
            <hr>
            <p>Frédéric Charain, organisateur du salon du chocolat de Paris, souhaite faire développer un site web pour communiquer sur son évènement ; celui-ci aura lieu dans 6 mois (à compter de la date de début du projet).<br/> Il souhaite pouvoir y présenter :</p>
            <ul>
                <li>- la liste des exposants;</li>
                <li>- la carte des exposants;</li>
                <li>- les intervenants prévus lors des tables rondes;</li>
                <li>- la liste des actualités et des retombées presse.</li>
            </ul>
            <p>Il souhaite aussi que les billets d’entrée puissent être commandés directement en ligne sur le site.</p>
            <br/>
        </article>

        <article>
            <h3>Instructions</h3>
            <hr>
            <p>Dans un premier temps, vous initierez le projet en adressant au client :</p>
            <ul>
                <li>- Une proposition commerciale comprenant une note de cadrage ainsi qu'un devis.</li>
            </ul>
            <p>Une fois l’appel d’offre remporté, vous lancerez le projet en livrant à votre directeur :</p>
            <ul>
                <li>- un cahier des charges;</li>
                <li>- un budget prévisionnel;</li>
                <li>- un planning de réalisation sous forme de diagramme de Gantt</li>
            </ul>
            <p>Conseil : Veillez à formuler une solution qui réponde aux besoins du client et respecte son budget ; sans pour autant compromettre l’intérêt financier de votre agence. Autrement dit, ne vendez le projet ni trop cher, ni à perte.</p>
            <br/>
        </article>

        <article>
            <h3>Compétences à valider</h3>
            <hr>
            <ul>
                <li>- Présenter une réponse à l'appel d'offres et rédiger un devis et le cahier des charges qui y est lié</li>
                <li>- Elaborer les différents éléments d’un projet</li>
                <li>- Rédiger un cahier des charges fonctionnel</li>
                <li>- Rédiger le cahier des charges technique</li>
                <li>- Construire un planning de réalisation</li>
                <li>- Formaliser un document contenant le dossier de création, le cahier des charges et tous les documents utiles aux mises à jour et aux évolutions d’un projet</li>
                <li>- Soutenir et argumenter ses propositions</li>
                <li>- Rédiger un organigramme fonctionnel pour la réalisation d’un projet</li>
                <li>- Mettre en place un diagramme organisationnel (Gantt, PERT, etc.) à l’aide de logiciels appropriés</li>
            </ul>
            <br/>
        </article>
